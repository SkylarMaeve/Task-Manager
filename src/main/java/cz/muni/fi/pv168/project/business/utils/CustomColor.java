package cz.muni.fi.pv168.project.business.utils;

/**
 * @author Vladimir Borek
 */
public class CustomColor {
    private final int r;
    private final int g;
    private final int b;

    public CustomColor(int r, int g, int b) {
        this.r = r;
        this.g = g;
        this.b = b;
    }

    public int getR() {
        return r;
    }

    public int getG() {
        return g;
    }

    public int getB() {
        return b;
    }
}

